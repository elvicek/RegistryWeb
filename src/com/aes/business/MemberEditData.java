package com.aes.business;

public class MemberEditData {
	
	private String baptised;
	private String birthday;
	private String convertedDate;
	
	
	
	
	public String getBaptised() {
		return baptised;
	}
	public void setBaptised(String baptised) {
		this.baptised = baptised;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getConvertedDate() {
		return convertedDate;
	}
	public void setConvertedDate(String convertedDate) {
		this.convertedDate = convertedDate;
	}
	
	
	

}
